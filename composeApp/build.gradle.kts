abstract class GenerateBuildConfigTask : DefaultTask() {
    @get:Input
    @get:Optional
    abstract val apiKey: Property<String>

    @get:OutputFile
    abstract val outputFile: RegularFileProperty

    @TaskAction
    fun generate() {
        val tmdbApiKey = apiKey.orNull ?: throw GradleException(
            """
            TMDB API key not found!

            Please add your API key to local.properties:
                tmdb.api.key=your_api_key_here

            OR set the environment variable:
                export TMDB_API_KEY=your_api_key_here

            Get your free API key at: https://www.themoviedb.org/settings/api
            """.trimIndent()
        )

        val file = outputFile.get().asFile
        file.parentFile.mkdirs()
        file.writeText("""
            |package com.cinescope.cinescope
            |
            |/**
            | * Auto-generated BuildConfig file
            | * DO NOT EDIT MANUALLY - This file is generated by the generateBuildConfig Gradle task
            | *
            | * To update, modify local.properties or set TMDB_API_KEY environment variable
            | */
            |object BuildConfig {
            |    const val TMDB_API_KEY = "$tmdbApiKey"
            |    const val TMDB_BASE_URL = "https://api.themoviedb.org/3"
            |    const val IMAGE_BASE_URL = "https://image.tmdb.org/t/p"
            |}
            |
        """.trimMargin())

        println("âœ… BuildConfig.kt generated successfully!")
    }
}

tasks.register<GenerateBuildConfigTask>("generateBuildConfig") {
    val localPropertiesFile = rootProject.file("local.properties")
    val localProperties = Properties()

    val key = if (localPropertiesFile.exists()) {
        localProperties.load(FileInputStream(localPropertiesFile))
        localProperties.getProperty("tmdb.api.key")
    } else {
        null
    } ?: System.getenv("TMDB_API_KEY")

    if (key != null) {
        apiKey.set(key)
    }

    outputFile.set(project.file("src/commonMain/kotlin/com/cinescope/cinescope/BuildConfig.kt"))
}

tasks.matching { task ->
    task.name.startsWith("compileKotlin")
}.configureEach {
    dependsOn("generateBuildConfig")
}
